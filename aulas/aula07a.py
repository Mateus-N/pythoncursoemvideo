n1=int(input('Um valor: '))
n2=int(input('Outro valor: '))
s=n1+n2
su=n1-n2
m=n1*n2
d=n1/n2
di=n1//n2
p=n1**n2
print('A soma entre eles vale {}, a subtração vale {} e a multiplicação vale {}!'.format(s,su,m))
print('Já a divisão entre eles vale {:.3f}, a divisão inteira vale {} e a potência vale {}!'.format(d,di,p))
